public class Circle {
    public static void main(String[]args){
        double numArea;
        double numRadius = 9.5;
        double mathPI = 3.14;
        double numPerimeter;

        numArea = numRadius * numRadius * mathPI;
        numPerimeter = 2 * numRadius * mathPI;

        System.out.println("The area of this is circle is " + numArea);
        System.out.println("The perimeter of this is circle is " + numPerimeter);





    }
}
